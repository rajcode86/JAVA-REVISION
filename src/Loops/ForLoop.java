package Loops;

public class ForLoop {

	public static void main(String[] args) {
		
		
	for (int i=1; i<=10; i++) {
		
		System.out.println(i);
	}
// In for loop we know where to start and where to stop range. number of iteration are known.
	/*In for loop the initialization, condition checking and updating of loop element is done in one line.
	 * For (initialization;condition;update)
	 * 
	 * 
	 */
	}

}
