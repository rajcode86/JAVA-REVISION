package Loops;

public class WhileLoop {

	public static void main(String[] args) {
		/* Loops are the way of repeating lines of code until condition is met.
		 * 1.While loop--> while loop repeats a block of code until the condition is true.
		 * 2.Do while loop
		 * 3.For loop 
		 * 4.Enhanced For loop 
		 * Loops are not statements. they are not terminated but exits.
		 * 
		 * any loop works in java based on two criteria :-1.entry criteria-->loop condition should be true
		 *                                                 2.exit criteria-->can only exit the loop when the condition is false.
		 * number of iterations are unknown
		 */
   int i =1;
  while (i<=10) {	 //if we remove curly braces then while loop continues.semi colon acts as a break point of loop.
	  System.out.println(i);
	  i++;
  }
		
		
		
		
		
		
		
		
	}

}
